package Giris;

public class Int {
    public static void main(String[] args) {
        // byte
        byte vByte = 100;
        System.out.println(vByte);
        // short
        short vShort = 1000;
        System.out.println(vShort);
        // integer
        int vInt = 10000;
        System.out.println(vInt);
        // long
        long vLong = 1000000;
        System.out.println(vLong);
    }
}
